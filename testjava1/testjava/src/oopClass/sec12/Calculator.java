package oopClass.sec12;

public class Calculator {
    static double pi = 3.14159; // 객체 생성 없이 사용가능한 필드이므로 보통 선언 할 때 초기값 성정

    static int pulus(int a, int b) {
        return a + b; // 두 정수의 합을 반환하는 메서드
    }

    static int minus(int a, int b) {
        return a - b; // 두 정수의 차를 반환하는 메서드
    }



}
